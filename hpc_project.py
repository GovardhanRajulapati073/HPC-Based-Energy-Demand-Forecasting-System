import numpy as np
from numba import njit, prange

@njit(parallel=True)
def create_windows(series, window=60):
    n = len(series) - window
    X = np.zeros((n, window))
    y = np.zeros(n)

    for i in prange(n):
        X[i] = series[i:i+window]
        y[i] = series[i+window]
    return X, y
