class LSTM(nn.Module):
    def __init__(self):
        super().__init__()
        self.lstm = nn.LSTM(1, 64, batch_first=True)
        self.fc = nn.Linear(64,1)

    def forward(self,x):
        o,_ = self.lstm(x)
        return self.fc(o[:,-1])

model = LSTM().cuda()
