import torch
import torch.nn as nn
from torch.utils.data import TensorDataset, DataLoader

# Move data to CPU first, then create DataLoader for batched transfer to GPU
X_t_cpu = torch.tensor(X1, dtype=torch.float32).unsqueeze(-1)
